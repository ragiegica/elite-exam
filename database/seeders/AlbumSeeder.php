    /**
     * Run the database seeds.
     */
    public function run(): void
    {
        $artists = $this->getArtists();
        $names = $this->getAlbumNames();
        $sales2022 = $this->getSales2022();
        $releaseDates = $this->getReleaseDates();
        $lastUpdates = $this->getLastUpdates();

        $albumsData = [];
        for ($i = 0; $i < count($artists); $i++) {
            $artist = Artist::where('name', $artists[$i])->first();
            if ($artist) {
                $albumsData[] = [
                    'artist_id' => $artist->id,
                    'name' => $names[$i],
                    'sales_2022' => $sales2022[$i],
                    'date_release' => Carbon::createFromFormat('ymd', $releaseDates[$i])->format('Y-m-d'),
                    'last_update' => Carbon::createFromFormat('ymd', $lastUpdates[$i])->format('Y-m-d'),
                    'created_at' => Carbon::now(),
                    'updated_at' => Carbon::now(),
                ];
            }
        }

        Album::insert($albumsData);

    }

